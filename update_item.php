<?php
include 'db.php';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $sql = "UPDATE kashf SET
        الصنف = ?,
        وحدة_القياس = ?,
        عدد_الجاهز = ?,
        عدد_يحتاج_صيانة = ?,
        عدد_تالف = ?,
        مكان_وجود_العهدة = ?,
        الجهة_التي_تتبعها_العهدة = ?,
        تأربخ_الادخال = ?,
        اسم_الشخص_المسؤول_عن_العهدة = ?,
        اسم_الشخص_المسؤول_على_القسم = ?
        WHERE id = ?";

    $stmt = $conn->prepare($sql);
    $stmt->bind_param(
        "ssiiissssii",
        $_POST['الصنف'],
        $_POST['وحدة_القياس'],
        $_POST['عدد_الجاهز'],
        $_POST['عدد_يحتاج_صيانة'],
        $_POST['عدد_تالف'],
        $_POST['مكان_وجود_العهدة'],
        $_POST['الجهة_التي_تتبعها_العهدة'],
        $_POST['تأربخ_الادخال'],
        $_POST['اسم_الشخص_المسؤول_عن_العهدة'],
        $_POST['اسم_الشخص_المسؤول_على_القسم'],
        $_POST['id']
    );

    if ($stmt->execute()) {
        header("Location: items_management.php");
    } else {
        echo "حدث خطأ أثناء تحديث البيانات: " . $conn->error;
    }
}
?>